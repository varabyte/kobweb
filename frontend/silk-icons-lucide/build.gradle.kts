import java.net.URI
import java.util.zip.ZipInputStream

plugins {
    alias(libs.plugins.kotlin.multiplatform)
    id("kobweb-compose")
    id("com.varabyte.kobweb.internal.publish")
}

group = "com.varabyte.kobwebx"
version = libs.versions.kobweb.get()

private val GENERATED_SRC_ROOT = "build/generated/icons/src/jsMain/kotlin"
val GENERATED_TXT_FILE = "lucide-icon-list.txt"
val LUCIDE_REPO_BASE = "https://github.com/lucide-icons/lucide"
val LUCIDE_VERSION = "0.574.0"

val fetchLucideIconsTask = tasks.register("fetchLucideIcons") {
    val outputFile = layout.projectDirectory.file(GENERATED_TXT_FILE)
    val lucideVersion = LUCIDE_VERSION
    val lucideRepoBase = LUCIDE_REPO_BASE

    doLast {
        println("Fetching icons for version $lucideVersion...")

        val activeIcons = mutableListOf<String>()
        val deprecatedIcons = mutableMapOf<String, String>()

        val zipUrl = "${lucideRepoBase}/archive/refs/tags/$lucideVersion.zip"
        val connection = URI(zipUrl).toURL().openConnection()
        connection.setRequestProperty("User-Agent", "kobweb-silk-icons-lucide-generator")

        ZipInputStream(connection.getInputStream()).use { zis ->
            while (true) {
                val entry = zis.nextEntry ?: break
                if (!entry.isDirectory && entry.name.contains("/icons/") && entry.name.endsWith(".json")) {
                    val iconName = entry.name.substringAfterLast("/").removeSuffix(".json")
                    activeIcons.add(iconName)

                    val content = zis.readBytes().decodeToString()
                    val aliasesBlock = "\"aliases\"\\s*:\\s*\\[(.*?)\\]".toRegex(RegexOption.DOT_MATCHES_ALL).find(content)?.groupValues?.get(1)
                    if (aliasesBlock != null) {
                        val aliasObjects = "\\{(.*?)\\}".toRegex(RegexOption.DOT_MATCHES_ALL).findAll(aliasesBlock)
                        for (aliasObj in aliasObjects) {
                            val objContent = aliasObj.groupValues[1]
                            val nameMatch = "\"name\"\\s*:\\s*\"([^\"]+)\"".toRegex().find(objContent)
                            val isDeprecated = "\"deprecated\"\\s*:\\s*true".toRegex().containsMatchIn(objContent)
                            if (nameMatch != null && isDeprecated) {
                                deprecatedIcons[nameMatch.groupValues[1]] = iconName
                            }
                        }
                    }
                }
            }
        }

        activeIcons.sort()

        outputFile.asFile.bufferedWriter().use { writer ->
            writer.write(
                """
                |# Lucide icon list — version $lucideVersion
                |#
                |# This file is used by the `generateIcons` Gradle task to generate Kotlin composable wrappers
                |# for Lucide icons (https://lucide.dev/).
                |#
                |# DO NOT EDIT THIS FILE BY HAND.
                |# Instead, run the `fetchLucideIcons` Gradle task to regenerate it from the latest Lucide release.
                |#
                |# Format:
                |#   lucide=icon1,icon2,...   — active icons
                |#   deprecated=icon1>canonical1,icon2>canonical2,...   — deprecated aliases (icon>canonical)
                |
                """.trimMargin()
            )
            writer.newLine()
            writer.write("lucide=${activeIcons.joinToString(",")}")
            writer.newLine()
            if (deprecatedIcons.isNotEmpty()) {
                val sortedDeprecated = deprecatedIcons.keys.sorted()
                writer.write("deprecated=${sortedDeprecated.joinToString(",") { "$it>${deprecatedIcons[it]}" }}")
                writer.newLine()
            }
        }
        println("Written to ${outputFile.asFile.name}")
    }
}

val generateIconsTask = tasks.register("generateIcons") {
    val srcFile = layout.projectDirectory.file(GENERATED_TXT_FILE)
    val dstFile =
        layout.projectDirectory.file("$GENERATED_SRC_ROOT/com/varabyte/kobweb/silk/components/icons/lucide/LucideIcons.kt")
    val lucideVersion = LUCIDE_VERSION
    val generatedTxtFile = GENERATED_TXT_FILE

    inputs.files(srcFile)
    outputs.dir(GENERATED_SRC_ROOT)

    doLast {
        val lines = srcFile.asFile
            .readLines()
            .filter { line -> line.isNotBlank() && !line.startsWith("#") }

        // Parse active icons: "lucide=icon1,icon2,..."
        val activeIcons = lines
            .firstOrNull { it.startsWith("lucide=") }
            ?.removePrefix("lucide=")
            ?.split(",")
            ?.filter { it.isNotBlank() }
            ?: emptyList()

        // Parse deprecated icons: "deprecated=icon1>canonical1,icon2>canonical2,..."
        // Each entry is "deprecatedName>canonicalName"
        val deprecatedIcons: Map<String, String> = lines
            .firstOrNull { it.startsWith("deprecated=") }
            ?.removePrefix("deprecated=")
            ?.split(",")
            ?.filter { it.isNotBlank() }
            ?.associate { entry ->
                val parts = entry.split(">", limit = 2)
                if (parts.size != 2) throw GradleException("Invalid deprecated entry: $entry")
                parts[0] to parts[1]
            }
            ?: emptyMap()

        fun rawNameToMethodName(rawName: String) = "Lucide" + rawName.split("-").joinToString("") {
            it.replaceFirstChar {
                if (it.isLowerCase()) it.titlecase() else it.toString()
            }
        }

        val iconParams = "modifier: Modifier = Modifier, absoluteStrokeWidth: Boolean = false, color: String? = null, size: Int? = null, strokeWidth: Number? = null"
        val iconArgs = "modifier, absoluteStrokeWidth, color, size, strokeWidth"

        val activeMethodEntries = activeIcons.map { rawName ->
            val methodName = rawNameToMethodName(rawName)
            "@Composable fun $methodName($iconParams) = LucideIcon(\"$rawName\", $iconArgs)"
        }

        val activeMethodNames = activeIcons.map { rawNameToMethodName(it) }.toSet()

        val deprecatedMethodEntries = deprecatedIcons.entries
            .sortedBy { it.key }
            .mapNotNull { (deprecatedName, canonicalName) ->
                val deprecatedMethodName = rawNameToMethodName(deprecatedName)
                // Skip if the deprecated method name collides with an active icon's method name
                if (deprecatedMethodName in activeMethodNames) return@mapNotNull null
                val canonicalMethodName = rawNameToMethodName(canonicalName)
                "@Deprecated(\"Use $canonicalMethodName instead.\", ReplaceWith(\"$canonicalMethodName(modifier)\"))\n@Composable fun $deprecatedMethodName($iconParams) = LucideIcon(\"$canonicalName\", $iconArgs)"
            }

        val activeIconsBlock = buildString {
            append(activeMethodEntries.joinToString("\n"))
        }

        val deprecatedBlock = if (deprecatedMethodEntries.isNotEmpty()) {
            "\n// Deprecated icon aliases\n" + deprecatedMethodEntries.joinToString("\n")
        } else {
            ""
        }

        val iconsCode =
            """
            |//@formatter:off
            |@file:Suppress("unused", "SpellCheckingInspection")
            |
            |// !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
            |// THIS FILE IS AUTOGENERATED.
            |//
            |// Do not edit this file by hand. Instead, run the `fetchLucideIcons` Gradle task to update
            |// `$generatedTxtFile` in the module root, then run the Gradle task "generateIcons".
            |// !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
            |
            |package com.varabyte.kobweb.silk.components.icons.lucide
            |
            |import androidx.compose.runtime.*
            |import com.varabyte.kobweb.compose.ui.Modifier
            |import com.varabyte.kobweb.compose.ui.toAttrs
            |import org.jetbrains.compose.web.dom.I
            |
            |/**
            | * Renders a Lucide icon using the `<i data-lucide="name">` convention.
            | *
            | * Requires the Lucide JavaScript library to be loaded on the page, e.g. via CDN:
            | * ```html
            | * <script src="https://unpkg.com/lucide@$lucideVersion/dist/umd/lucide.min.js"></script>
            | * <script type="module">lucide.createIcons()</script>
            | * ```
            | * Version ${lucideVersion} is recommended.
            | *
            | * See also: https://lucide.dev/
            | */
            |@Composable
            |fun LucideIcon(
            |    name: String,
            |    modifier: Modifier = Modifier,
            |    absoluteStrokeWidth: Boolean = false,
            |    color: String? = null,
            |    size: Int? = null,
            |    strokeWidth: Number? = null,
            |) {
            |    val effectiveStrokeWidth: Double? = when {
            |        absoluteStrokeWidth -> (strokeWidth?.toDouble() ?: 2.0) * 24.0 / (size?.toDouble() ?: 24.0)
            |        strokeWidth != null -> strokeWidth.toDouble()
            |        else -> null
            |    }
            |    I(
            |        attrs = modifier.toAttrs {
            |            attr("data-lucide", name)
            |            if (color != null) attr("stroke", color)
            |            if (size != null) {
            |                attr("height", size.toString())
            |                attr("width", size.toString())
            |            }
            |            if (effectiveStrokeWidth != null) attr("stroke-width", effectiveStrokeWidth.toString())
            |        }
            |    )
            |}
            |
            |$activeIconsBlock$deprecatedBlock
            """.trimMargin()

        dstFile.asFile.apply {
            parentFile.mkdirs()
            writeText(iconsCode)
        }
    }
}

